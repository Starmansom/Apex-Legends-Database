create table olympus (location_id int primary key, location varchar(50) not null,common_loot int default 0, epic_loot int default 0, rare_loot int default 0,legendary_loot int default 0 ,loot_tier varchar(20) default 'NONE' );
create table items(itemsID int primary key,name varchar(20) unique not null, category varchar(20) not null,use_time int default 0,max_per_slot int not null default 1, rarity varchar(20) default 'c',description varchar(100));
create table player(ID int primary key,name varchar(20),username varchar(20) unique,password varchar(20),email varchar(20) unique,date_of_join date, acc_type varchar(20),credits int default 0);
create table ranks(player_id int primary key, total_matches int , p_rank varchar(20), rp int default 0, kills int default 0, deaths int default 0, foreign key(player_id) references player(ID));
create table stats (player_id int primary key , lifetime_games int default 0, level int default 1, bp_level int default 1, avg_playtime float default 0.0, lifetime_wins int default 0, lifetime_kills int default 0, foreign key(player_id) references player(ID));
create table weapons(weaponID int primary key, name varchar(20) not null, category varchar(20),ammo_type varchar(20),damage int check (damage>0), RPM int check (RPM>0), capacity int not null, reload_time float not null);
create table legends(legendID int primary key, name varchar(20) unique not null,category varchar(20) not null, win_rate float default 0.0, passive varchar(50),tactical varchar(50),ultimate varchar(50));
create table seasons(season_number int primary key,name varchar(20) default 'firefight',legend_added int, weapon_added int, active_map varchar(20), limited_event varchar(20),updates int default 0, release_date date,foreign key (legend_added) references legends(legendID),foreign key (weapon_added) references weapons(weaponID));


